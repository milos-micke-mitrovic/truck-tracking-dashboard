function createTerminals(count: number, baseAddress: string): CompanyTerminal[] {
  return Array.from({ length: count }, (_, i) => ({
    id: `terminal-${i + 1}`,
    address: i === 0 ? baseAddress : `Terminal ${i + 1} - ${baseAddress}`,
    timezone: 'America/Chicago',
    startingTime: '00:00:00',
  }))
}

